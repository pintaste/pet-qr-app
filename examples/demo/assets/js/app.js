// Verify PIN
function verifyPin() {
    const pinDigits = document.querySelectorAll('.pin-digit');
    const pinError = document.getElementById('pinError');
    const verifyBtn = document.getElementById('verifyBtn');
    
    const pin = Array.from(pinDigits).map(input => input.value).join('');
    
    if (pin === correctPin) {
        showPage('pet');
        if (pinError) pinError.textContent = '';
        // Clear PIN inputs
        pinDigits.forEach(input => input.value = '');
        if (verifyBtn) verifyBtn.disabled = true;
    } else {
        if (pinError) {
            pinError.textContent = translations[currentLanguage].incorrectPin;
        }
        // Clear PIN inputs after error
        setTimeout(() => {
            pinDigits.forEach(input => input.value = '');
            if (pinDigits[0]) pinDigits[0].focus();
            if (verifyBtn) verifyBtn.disabled = true;
        }, 1000);
    }
}

