// Setup PIN inputs
function setupPinInputs() {
    const pinDigits = document.querySelectorAll('.pin-digit');
    
    pinDigits.forEach((input, index) => {
        input.addEventListener('input', (e) => {
            if (e.target.value.length === 1) {
                if (index < pinDigits.length - 1) {
                    pinDigits[index + 1].focus();
                }
            }
            checkPinComplete();
        });

        input.addEventListener('keydown', (e) => {
            if (e.key === 'Backspace' && e.target.value === '' && index > 0) {
                pinDigits[index - 1].focus();
            }
        });

        // Only allow numbers
        input.addEventListener('keypress', (e) => {
            if (!/\d/.test(e.key) && !['Backspace', 'Delete', 'ArrowLeft', 'ArrowRight'].includes(e.key)) {
                e.preventDefault();
            }
        });
    });
}

// Check if PIN is complete
function checkPinComplete() {
    const pinDigits = document.querySelectorAll('.pin-digit');
    const verifyBtn = document.getElementById('verifyBtn');
    const pinError = document.getElementById('pinError');
    
    const pin = Array.from(pinDigits).map(input => input.value).join('');
    if (verifyBtn) {
        verifyBtn.disabled = pin.length !== 4;
    }
    if (pinError) {
        pinError.textContent = '';
    }
}

// Setup gallery functionality
function setupGallery() {
    const thumbnails = document.querySelectorAll('.gallery-thumb');
    const mainImage = document.getElementById('mainImage');
    const currentImageSpan = document.getElementById('currentImage');
    
    thumbnails.forEach((thumb, index) => {
        thumb.addEventListener('click', () => {
            // Remove active class from all thumbnails
            thumbnails.forEach(t => t.classList.remove('active'));
            // Add active class to clicked thumbnail
            thumb.classList.add('active');
            
            // Update main image
            if (mainImage) {
                mainImage.src = thumb.dataset.src;
            }
            
            // Update counter
            if (currentImageSpan) {
                currentImageSpan.textContent = index + 1;
            }
        });
    });
}

// Select language
function selectLanguage(lang) {
    console.log('Language selected:', lang);
    currentLanguage = lang;
    updateLanguage();
    showPage('pin');
}

// Update language
function updateLanguage() {
    const t = translations[currentLanguage];
    
    Object.keys(t).forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            if (element.tagName === 'INPUT' && element.placeholder !== undefined) {
                element.placeholder = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });
    
    // Update placeholders for specific inputs
    const locationInput = document.getElementById('locationInput');
    const messageInput = document.getElementById('messageInput');
    const emailInput = document.getElementById('emailInput');
    const passwordInput = document.getElementById('passwordInput');
    
    if (locationInput) {
        locationInput.placeholder = t.locationPlaceholder;
    }
    if (messageInput) {
        messageInput.placeholder = t.messagePlaceholder;
    }
    if (emailInput) {
        emailInput.placeholder = t.emailPlaceholder;
    }
    if (passwordInput) {
        passwordInput.placeholder = t.passwordPlaceholder;
    }
    
    // Update language code in header
    const currentLangCode = document.getElementById('currentLangCode');
    if (currentLangCode) {
        currentLangCode.textContent = t.langCode;
    }
    
    // Update active state in dropdown
    updateLanguageDropdownStates();
}

// Language switcher functions
function toggleLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
        dropdown.classList.toggle('active');
    }
}

function closeLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
        dropdown.classList.remove('active');
    }
}

function updateLanguageDropdownStates() {
    const languageOptions = document.querySelectorAll('.language-option');
    languageOptions.forEach(option => {
        if (option.dataset.lang === currentLanguage) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });
}

