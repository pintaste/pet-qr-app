// Location functions
function getCurrentLocation() {
    const locationInput = document.getElementById('locationInput');
    const getLocationBtn = document.getElementById('getLocationBtn');
    const getLocationText = document.getElementById('getLocationText');
    
    if (!navigator.geolocation) {
        if (locationInput) {
            locationInput.value = 'Geolocation not supported by this browser';
        }
        return;
    }

    // Update button state to loading
    if (getLocationText) {
        getLocationText.textContent = 'Getting location...';
    }
    if (getLocationBtn) {
        getLocationBtn.disabled = true;
    }

    // Clear any previous location data
    if (locationInput) {
        locationInput.value = '';
    }

    const options = {
        enableHighAccuracy: true,
        timeout: 10000,
        maximumAge: 60000
    };

    navigator.geolocation.getCurrentPosition(
        (position) => {
            console.log('Location obtained:', position);
            const lat = position.coords.latitude.toFixed(6);
            const lng = position.coords.longitude.toFixed(6);
            
            if (locationInput) {
                locationInput.value = `${lat}, ${lng}`;
            }
            
            // Reset button state
            if (getLocationText) {
                getLocationText.textContent = translations[currentLanguage].getLocationText;
            }
            if (getLocationBtn) {
                getLocationBtn.disabled = false;
            }
            
            console.log('Location updated successfully');
        },
        (error) => {
            console.error('Geolocation error:', error);
            let errorMessage = 'Location access denied';
            
            switch(error.code) {
                case error.PERMISSION_DENIED:
                    errorMessage = 'Location access denied by user';
                    break;
                case error.POSITION_UNAVAILABLE:
                    errorMessage = 'Location information unavailable';
                    break;
                case error.TIMEOUT:
                    errorMessage = 'Location request timed out';
                    break;
                default:
                    errorMessage = 'An unknown error occurred';
                    break;
            }
            
            if (locationInput) {
                locationInput.value = errorMessage;
            }
            
            // Reset button state
            if (getLocationText) {
                getLocationText.textContent = translations[currentLanguage].getLocationText;
            }
            if (getLocationBtn) {
                getLocationBtn.disabled = false;
            }
        },
        options
    );
}

